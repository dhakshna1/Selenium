package day1.examples;

public class ExampleIf {

	public static void main(String[] args) {
		int x=10;
		int y=10;
		if( x<y ) {
			System.out.println("X is less than Y");
		}
		else if (x>y) {
			System.out.println("X is greater than Y");
		}
		else if (x==y){
			System.out.println("X and Y are equal");
		}

	}

}
